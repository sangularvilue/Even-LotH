/** Draw a character at (x, y) on a pixel buffer of given width. */
export function drawChar(
  pixels: Uint8Array,
  bufWidth: number,
  x: number,
  y: number,
  ch: string,
): void {
  const glyph = FONT[ch] ?? FONT[ch.toUpperCase()];
  if (!glyph) return;
  for (let row = 0; row < FONT_H; row++) {
    const bits = glyph[row]!;
    for (let col = 0; col < FONT_W; col++) {
      if (bits & (1 << (FONT_W - 1 - col))) {
        const px = x + col;
        const py = y + row;
        if (px >= 0 && px < bufWidth && py >= 0) {
          pixels[py * bufWidth + px] = 1;
        }
      }
    }
  }
}

/** Draw a string at (x, y) with 1px spacing between characters. */
export function drawText(
  pixels: Uint8Array,
  bufWidth: number,
  x: number,
  y: number,
  text: string,
): void {
  let cx = x;
  for (const ch of text) {
    drawChar(pixels, bufWidth, cx, y, ch);
    cx += FONT_W + 1;
  }
}

/** Measure text width in pixels. */
export function measureText(text: string): number {
  if (text.length === 0) return 0;
  return text.length * (FONT_W + 1) - 1;
}
